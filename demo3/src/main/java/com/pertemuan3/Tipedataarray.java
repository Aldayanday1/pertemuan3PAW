package com.pertemuan3;

public class Tipedataarray {
    public static void main(String[] args) {
        int[] angka = {1, 2, 3, 4, 5}; 
        String[] buah = new String[3];
        buah[0] = "Apel";
        buah[1] = "Pisang";
        buah[2] = "Jeruk";
        System.out.println("Angka[2]: " + angka[2]);
        System.out.println("Buah[1]: " + buah[1]);
    }
}
