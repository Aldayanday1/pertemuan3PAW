package com.pertemuan3;

public class Augmentedassignments {
    public static void main(String[] args) {
        int angka1 = 10;
        int angka2 = 5;
        angka1 += angka2; // Sama dengan angka1 = angka1 + angka2;
        System.out.println("angka1: " + angka1);
        double nilai = 15.5;
        nilai *= 2; // Sama dengan nilai = nilai * 2;
        System.out.println("nilai: " + nilai);
    }
}
